let activeCard = 1;

function renderMenu() {
    const menu = document.getElementById('menu');
    menu.innerHTML = '<h2>Меню</h2>';

    data.forEach(item => {
        const card = document.createElement('div');
        card.className = `card2 ${item.id === activeCard ? 'active' : ''}`;
        card.innerHTML = `<h3>${item.title}</h3><p>${item.subtitle}</p>`;
        card.onclick = () => selectCard(item.id);
        menu.appendChild(card);
    });
}

function selectCard(id) {
    activeCard = id;
    renderMenu();
    renderContent();
}

function renderContent() {
    const item = data.find(d => d.id === activeCard);

    document.getElementById('textSection').innerHTML = item.text;

    const imageSection = document.getElementById('imageSection');
    imageSection.innerHTML = '';
    item.images.forEach(src => {
        const img = document.createElement('img');
        img.src = src;
        img.alt = item.title;
        imageSection.appendChild(img);
    });
}

renderMenu();
renderContent();